/** Prints out a pre-formatted separator line to the console. */
void printSeparator(void) {
  printf("\n\n\n");

  for (int nPrintedDashAmount = 0; nPrintedDashAmount < MAX_PRINT_WIDTH; nPrintedDashAmount++) printf("‚ĒĀ");

  printf("\n\n\n\n");
}

